CREATE TABLE CAR_USER (
    CAR_NO VARCHAR(20) PRIMARY KEY,
    REG_DATE DATE NOT NULL,
    LAST_USED DATE,
    IS_AGREED CHAR(1) NOT NULL CHECK (IS_AGREED IN ('Y','N')),
    INPUT_METHOD VARCHAR(10) NOT NULL CHECK (INPUT_METHOD IN ('AUTO', 'MANUAL'))
);
