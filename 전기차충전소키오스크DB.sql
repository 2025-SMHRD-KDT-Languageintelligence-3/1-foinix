CREATE TABLE PAYMENT_METHOD (
    CARD_NO VARCHAR(20) PRIMARY KEY,
    EXP_DATE VARCHAR(5) NOT NULL,
    CARD_ISSUER VARCHAR(30) NOT NULL,
    CAR_NO VARCHAR(20) NOT NULL,
    PAY_TYPE VARCHAR(20) NOT NULL CHECK (PAY_TYPE IN ('CARD','PAY')),
    CONSTRAINT FK_PAYMENT_USER FOREIGN KEY (CAR_NO) REFERENCES CAR_USER(CAR_NO)
);

CREATE TABLE CHARGER_POINT (
    CHARGER_ID INT PRIMARY KEY,
    CHARGER_NAME VARCHAR(50) NOT NULL,
    AREA_ID INT NOT NULL,
    ADDRESS VARCHAR(100) NOT NULL,
    CHARGER_COUNT INT NOT NULL,
    IS_ACTIVE CHAR(1) NOT NULL CHECK (IS_ACTIVE IN ('Y','N'))
);

CREATE TABLE CHARGER_UNIT (
    UNIT_ID INT PRIMARY KEY,
    CHARGER_ID INT NOT NULL,
    STATUS VARCHAR(10) NOT NULL CHECK (STATUS IN ('사용가능','사용중','고장')),
    POWER_KW INT NOT NULL,
    CHARGER_TYPE VARCHAR(20) NOT NULL,
    FOREIGN KEY (CHARGER_ID) REFERENCES CHARGER_POINT(CHARGER_ID)
);

CREATE TABLE PAYMENT (
    PAYMENT_ID INT PRIMARY KEY,
    AMOUNT DECIMAL(10,2) NOT NULL,
    PAYMENT_DATE DATE NOT NULL,
    STATUS VARCHAR(20) NOT NULL,
    CARD_NO VARCHAR(20) NOT NULL,
    CAR_NO VARCHAR(20) NOT NULL,
    FOREIGN KEY (CARD_NO) REFERENCES PAYMENT_METHOD(CARD_NO),
    FOREIGN KEY (CAR_NO) REFERENCES CAR_USER(CAR_NO)
);

CREATE TABLE CHARGE_LOG (
    LOG_ID INT PRIMARY KEY,
    CAR_NO VARCHAR(20) NOT NULL,
    UNIT_ID INT NOT NULL,
    START_TIME DATE NOT NULL,
    END_TIME DATE,
    POWER_USED DECIMAL(6,2),
    PAYMENT_ID INT UNIQUE,
    IS_SUCCESS CHAR(1) NOT NULL CHECK (IS_SUCCESS IN ('Y','N')),
    FOREIGN KEY (UNIT_ID) REFERENCES CHARGER_UNIT(UNIT_ID),
    FOREIGN KEY (PAYMENT_ID) REFERENCES PAYMENT(PAYMENT_ID)
);


