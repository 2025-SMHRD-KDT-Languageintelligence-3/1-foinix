CREATE TABLE FACILITY (
    FACILITY_ID INT PRIMARY KEY,
    NAME VARCHAR(50) NOT NULL,
    TYPE VARCHAR(20) NOT NULL,
    ADDRESS VARCHAR(100) NOT NULL,
    CHARGER_ID INT NOT NULL,
    FOREIGN KEY (CHARGER_ID) REFERENCES CHARGER_POINT(CHARGER_ID)
);

CREATE TABLE ADMINS (
    ADMIN_ID INT PRIMARY KEY,
    NAME VARCHAR(30) NOT NULL,
    PHONE VARCHAR(15) NOT NULL,
    EMAIL VARCHAR(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR(100) NOT NULL,
    ROLE VARCHAR(20) NOT NULL
);

CREATE TABLE ADMIN_CALL (
    CALL_ID INT PRIMARY KEY,
    CAR_NO VARCHAR(20) NOT NULL,
    CALL_TIME DATE NOT NULL,
    LOCATION VARCHAR(50) NOT NULL,
    ADMIN_ID INT,
    RESOLVED_TIME DATE,
    STATUS VARCHAR(10) NOT NULL CHECK (STATUS IN ('대기','완료')),
    FOREIGN KEY (ADMIN_ID) REFERENCES ADMINS(ADMIN_ID)
);

CREATE TABLE PERSONAL_DATA_CONSENT (
    CONSENT_ID INT PRIMARY KEY,
    USER_ID VARCHAR(20) NOT NULL,
    CONSENT_GIVEN CHAR(1) NOT NULL CHECK (CONSENT_GIVEN IN ('Y','N')),
    CONSENT_DATE DATETIME DEFAULT CURRENT_TIMESTAMP,
    CONSENT_TYPE VARCHAR(50) NOT NULL,
    CONSTRAINT FK_CONSENT_USER FOREIGN KEY (USER_ID) REFERENCES CAR_USER(CAR_NO)
);


